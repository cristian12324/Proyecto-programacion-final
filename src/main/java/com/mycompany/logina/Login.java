        // TODO add your handling code here:
        
        String usuario = jTextField1.getText();
        String password = jTextField2.getText();
       
        
        if("admin".equals(usuario)&&"admin".equals(password)){
            Administrador a = new Administrador();
            a.setVisible(true);
            this.dispose();
        }
        else if (EstudianteEncontrado(usuario, password)){
            BienvenidoAlumno a = new BienvenidoAlumno(usuario,password);
            a.setVisible(true);
            this.dispose();
         
            
        }
        else{
            boolean encontrado = false;
            for(ProfesorA p : LoginA.CreacionProfesor){
                if(p.password.equals(password)&&p.usuario.equals(usuario)){
                    ProfesorA profesorActual = profesorEncontrado(usuario, password);
                    BienvenidaProfesor b = new BienvenidaProfesor(profesorActual);
                    b.setVisible(true);
                    encontrado = true;
                    this.dispose();
                }
            }
            if(!encontrado){
                JOptionPane.showMessageDialog(this, "Usuario o password incorrecto.");
            }
        }
        
