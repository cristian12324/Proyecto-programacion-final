    private boolean EstudianteEncontrado(String usuario, String password){
        boolean encontrado = false;
        for(Alumnos a : LoginA.ListaAlumnos){
            if(a.carne.equals(usuario)&&a.password.equals(password)){
                encontrado = true;     
            }
        }
        return encontrado;
    }
    private ProfesorA profesorEncontrado(String usuario, String password){
        ProfesorA encontrado = null;
        for(ProfesorA p : LoginA.CreacionProfesor){
            System.out.println(p.password +" password de la Iteracion");
            if(p.usuario.equals(usuario)&&p.password.equals(password)){
                encontrado = p;
            }
        }
        return encontrado;
    }

